//  Divisible by 5
// Given a number, print whether it is divisible by 5.

// Input: 10 → Output: "divisible by 5"
// Input: 11 → Output: "not divisible by 5"
// Input: 0 → Output: "divisible by 5"

let check = 231;
if(check%5===0){
    console.log("divisible by 5")
}else{
    console.log("not divisible by 5")
}