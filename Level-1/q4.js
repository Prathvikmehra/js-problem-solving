// Positive, Negative, or Zero
// Given a number, print "positive", "negative", or "zero".

// Input: 8 → Output: "positive"
// Input: -2 → Output: "negative"
// Input: 0 → Output: "zero"

let a = 13;
if (a < 0) {
    console.log("negative");
} else if (a > 0) {
    console.log("positive");
} else {
    console.log("zero");
}